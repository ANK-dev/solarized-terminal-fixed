#!/bin/bash
# Solarized Colors - This script just echoes a simple table of colors

NC='\033[0m'    # NoColor
K='\033[1;30m'  # BRIGHT blacK   (Base 03)
k='\033[0;30m'  # blacK          (Base 02)
G='\033[1;32m'  # BRIGHT Green   (Base 01)
Y='\033[1;33m'  # BRIGHT Yellow  (Base 00)
B='\033[1;34m'  # BRIGHT Blue    (Base  0)
C='\033[1;36m'  # BRIGHT Cyan    (Base  1)
w='\033[0;37m'  # white          (Base  2)
W='\033[1;37m'  # BRIGHT White   (Base  3)
y='\033[0;33m'  # yellow
R='\033[1;31m'  # BRIGHT Red
r='\033[0;31m'  # red
m='\033[0;35m'  # magenta
M='\033[1;35m'  # BRIGHT Magenta
b='\033[0;34m'  # blue
c='\033[0;36m'  # cyan
g='\033[0;32m'  # green

echo
echo -e "╔══════════ Solarized Colors ══════════╗${NC}"
echo -e "║${NC}                                      ║${NC}"
echo -e "║${NC}${K}   █ Bright Black         (Base 03)${NC}   ║${NC}█"
echo -e "║${NC}${k}   █ Black                (Base 02)${NC}   ║${NC}█"
echo -e "║${NC}${G}   █ Bright Green         (Base 01)${NC}   ║${NC}█"
echo -e "║${NC}${Y}   █ Bright Yellow        (Base 00)${NC}   ║${NC}█"
echo -e "║${NC}${B}   █ Bright Blue          (Base  0)${NC}   ║${NC}█"
echo -e "║${NC}${C}   █ Bright Cyan          (Base  1)${NC}   ║${NC}█"
echo -e "║${NC}${w}   █ White [Foreground]   (Base  2)${NC}   ║${NC}█"
echo -e "║${NC}${W}   █ Bright White         (Base  3)${NC}   ║${NC}█"
echo -e "║${NC}${y}   █ Yellow                        ${NC}   ║${NC}█"
echo -e "║${NC}${R}   █ Bright Red                    ${NC}   ║${NC}█"
echo -e "║${NC}${r}   █ Red                           ${NC}   ║${NC}█"
echo -e "║${NC}${m}   █ Magenta                       ${NC}   ║${NC}█"
echo -e "║${NC}${M}   █ Bright Magenta                ${NC}   ║${NC}█"
echo -e "║${NC}${b}   █ Blue                          ${NC}   ║${NC}█"
echo -e "║${NC}${c}   █ Cyan                          ${NC}   ║${NC}█"
echo -e "║${NC}${g}   █ Green                         ${NC}   ║${NC}█"
echo -e "║${NC}                                      ║${NC}█"
echo -e "╚══════════════════════════════════════╝${NC}█"
echo -e "   ▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀▀\n"